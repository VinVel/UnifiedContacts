package net.velcore.unifiedcontacts.domain.util.validator

object EmailValidator {
    private val EMAIL_REGEX =
        Regex("(?:[a-z0-9!#\$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#\$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])")
    // this is the official RFC 5322 compliant Email Regex

    fun validate(address: String): ValidationResult {
        val emailAddress = address.trim()

        if (emailAddress.isEmpty()) {
            return ValidationResult.Invalid("Email cannot be empty.")
        }

        if (!EMAIL_REGEX.matches(emailAddress)) {
            return ValidationResult.Invalid("Format is invalid.")
        }

        return ValidationResult.Valid
    }
}